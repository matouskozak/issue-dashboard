# parse_issue_node INTEGRATION  (body → parsed issue → scores)
# =========================================================================


class TestParseIssueNodeIntegration:
    """End-to-end: raw GraphQL node → ParsedIssue with scores."""

    def test_full_pipeline(self):
        from datetime import datetime, timezone
        from scripts.fetch_issues import parse_issue_node

        now = datetime.now(timezone.utc)
        node = {
            "number": 99999,
            "title": "Integration test issue",
            "state": "OPEN",
            "createdAt": (now - __import__("datetime").timedelta(days=5)).isoformat(),
            "updatedAt": (now - __import__("datetime").timedelta(days=1)).isoformat(),
            "body": (
                "|24-Hour Hit Count|7-Day Hit Count|1-Month Count|\n"
                "|---|---|---|\n"
                "|10|50|200|\n"
                "\n"
                "```json\n"
                '{"ErrorMessage": "System.TimeoutException"}\n'
                "```\n"
                "\n"
                "https://dev.azure.com/dnceng/public/_build/results?buildId=7777\n"
            ),
            "author": {"login": "testuser"},
            "labels": {"nodes": [{"name": "Known Build Error"}, {"name": "area-GC"}]},
            "assignees": {"nodes": [{"login": "alice"}]},
            "comments": {
                "nodes": [
                    {
                        "author": {"login": "alice"},
                        "authorAssociation": "MEMBER",
                        "body": "Looking into it",
                        "createdAt": (
                            now - __import__("datetime").timedelta(days=1)
                        ).isoformat(),
                    }
                ]
            },
            "reactions": {"totalCount": 3},
        }

        issue = parse_issue_node(node, "runtime", now)

        # Hit counts parsed correctly
        assert issue.hits_24h == 10
        assert issue.hits_7d == 50
        assert issue.hits_30d == 200

        # Error pattern extracted
        assert issue.error_pattern is not None
        assert "TimeoutException" in issue.error_pattern

        # Build link extracted
        assert issue.build_link is not None
        assert "buildId=7777" in issue.build_link

        # Labels / assignees
        assert "area-GC" in issue.labels
        assert issue.area_label == "area-GC"
        assert "alice" in issue.assignees

        # Comment analysis
        assert issue.human_comment_count == 1

        # Scores are populated
        assert 0 <= issue.urgency_score <= 10
        assert 0 <= issue.staleness_score <= 10
        assert 0 <= issue.neglect_score <= 10
