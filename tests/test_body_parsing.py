# EDGE CASES
# =========================================================================


class TestEdgeCases:
    """Assorted edge cases that could trip up a naïve parser."""

    def test_non_numeric_values_default_to_zero(self):
        """Non-numeric cell content should not crash the parser."""
        body = (
            "|24-Hour Hit Count|7-Day Hit Count|1-Month Count|\n"
            "|---|---|---|\n"
            "|N/A|unknown|—|\n"
        )
        result = parse_hit_counts(body)
        # Regex requires \\d+, won't match non-numeric → defaults
        assert result.hits_24h == 0
        assert result.hits_7d == 0
        assert result.hits_30d == 0

    def test_very_large_numbers(self):
        """Parser handles very large hit counts without overflow."""
        body = (
            "|24-Hour Hit Count|7-Day Hit Count|1-Month Count|\n"
            "|---|---|---|\n"
            "|999999|1000000|9999999|\n"
        )
        result = parse_hit_counts(body)
        assert result.hits_24h == 999999
        assert result.hits_7d == 1000000
        assert result.hits_30d == 9999999

    def test_table_with_trailing_pipe(self):
        """Standard markdown tables include trailing pipes."""
        body = (
            "|24-Hour Hit Count|7-Day Hit Count|1-Month Count|\n"
            "|---|---|---|\n"
            "|10|50|200|\n"
        )
        result = parse_hit_counts(body)
        assert result.hits_24h == 10
        assert result.hits_7d == 50
        assert result.hits_30d == 200

    def test_body_with_only_whitespace(self):
        result = parse_hit_counts("   \n\n  \t  \n")
        assert result.hits_24h == 0
        assert result.hits_7d == 0
        assert result.hits_30d == 0

    def test_return_type_is_hitcounts(self, sample_issue_body_well_formed):
        from scripts.fetch_issues import HitCounts
        result = parse_hit_counts(sample_issue_body_well_formed)
        assert isinstance(result, HitCounts)

    def test_required_attrs_present(self, sample_issue_body_well_formed):
        result = parse_hit_counts(sample_issue_body_well_formed)
        assert hasattr(result, "hits_24h")
        assert hasattr(result, "hits_7d")
        assert hasattr(result, "hits_30d")


